// 2.pop()

//?it is used to remove the element the end of the array.

let arr2=["gani","kani","vinoth"]

arr2.pop()

console.log(arr2);


// 3.unshift()

//? it is used to add element  at the starting of the array. 

arr2.unshift("dhawan")

