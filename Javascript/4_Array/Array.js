// 4.shift()

//? it is used to remove the element at the starting of the array

arr2.shift()
console.log(arr2);

// 5.indexof()

//? it is used to get the index of the given element.

let Num=["html",'css','java']

console.log(Num.indexOf("css"));

// 6.include()

//? it is used to check whether the given element is present or not in the array.

console.log(Num.includes("html"));

// 7.concat()

//? it is used to combine two or more than two array.

let abb=[1,2,3]
let app=[4,5,6]

coc=abb.concat(app)

console.log(coc);

// 8.join()

